// 执行计划提示词模板函数 (对应英文原文件: /src/slash-commands/builtin/spec/execute-plan.ts)

export const executePlanPrompt = `
# 执行计划

## 概述

加载计划，进行批判性评估，分批执行任务，在批次之间报告以供审查。

**核心原则：** 批量执行与架构师审查的检查点。

**开始时宣布：** "我正在实施这个计划。"

## 过程

### 步骤1：加载和审查计划
1. 阅读计划文件
2. 进行批判性评估-识别计划中的任何问题或关注点
3. 如果有关注点：在开始前与合作伙伴提出
4. 如果没有关注点：创建待办事项并继续

### 步骤2：执行批次
**默认：前3个任务**

对于每个任务：
1. 标记为进行中
2. 严格按照步骤执行（计划有小步骤）
3. 按照指定运行验证
4. 标记为已完成

### 步骤3：报告
当批次完成后：
- 展示已实施的内容
- 展示验证输出
- 说："准备好接受反馈。"

### 步骤4：继续
基于反馈：
- 如需要，应用更改
- 执行下一批次
- 重复直到完成

### 步骤5：完成开发

在所有任务完成并验证后：
- 宣布："我已完成此实施。"

## 何时停止并寻求帮助

**立即停止执行当：**
- 在批次中遇到阻碍（缺少依赖、测试失败、指令不清楚）
- 计划存在关键差距无法开始
- 你不理解某个指令
- 验证反复失败

**寻求澄清而不是猜测。**

## 何时回到早期步骤

**回到审查（步骤1）当：**
- 合作伙伴根据你的反馈更新了计划
- 需要重新思考基本方法

**不要强行通过阻碍** - 停下来询问。

## 记住
- 首先批判性地评估计划
- 严格按照计划步骤执行
- 不要跳过验证
- 批次之间：只报告并等待
- 遇到阻碍时停止，不要猜测
`;
