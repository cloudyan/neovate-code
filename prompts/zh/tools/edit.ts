// Edit工具提示词模板函数

export function createEditPrompt() {
  return `
编辑本地文件系统中的文件。

用法：
- 在编辑之前，您必须至少使用一次 read 工具。如果您在没有读取文件的情况下尝试编辑，此工具将报错。
- 编辑从 Read 工具输出的文本时，请确保保留行号前缀后的精确缩进（制表符/空格）。行号前缀格式为：空格 + 行号 + 制表符。制表符之后的所有内容都是要匹配的实际文件内容。切勿在 old_string 或 new_string 中包含行号前缀的任何部分。
- 对于移动或重命名文件，通常应使用 Bash 工具和 'mv' 命令。
- 对于较大的编辑，请使用 Write 工具覆盖文件。
- 对于文件创建，请使用 Write 工具。
- 当连续对同一文件进行多次编辑时，建议在单个消息中发送所有编辑调用，而不是在多个消息中每次调用一个。
  `;
}
